                <h1 class="first">Hinter der Feuerwehr steht ein starker Verein</h1>
                <p>
                    Die Freiwillige Feuerwehr Bad Soden am Taunus steht für die Bürger der Stadt Bad Soden am Taunus 24 Stunden am Tag und 365 Tage im Jahre bereit, um in den verschiedensten 
                    Notlagen und Gefahrensituationen zu helfen und Schaden von der Allgemeinheit abzuwenden.<br />
                	Um die Einsatzabteilung zu unterstützen und zu fördern, wurde der Verein "Freiwillige Feuerwehr Bad Soden am Taunus e.V." gegründet. Mit Hilfe des Vereins gelang es uns zahlreiche Gerätschaften, 
                    die bei den Einsätzen verwendet werden, anzuschaffen und die Jugend, sowie die Ausbildung der aktiven Einsatzabteilung zu fördern.
                </p>
