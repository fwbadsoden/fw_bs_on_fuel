<h1>What is FUEL CMS?</h1>
<p>At it's core, FUEL CMS is a modular based, CodeIgniter development platform for 
creating web applications. You can create your models, views and controllers and 
only use the CMS part when and if you need it. It's a hybrid of a framework and a CMS.</p>

<p>FUEL CMS evolved out of the need for having a development platform that
was a framework first and a CMS second. CodeIgniter was chosen as the framework because it is
a popular, well-documentated, lightweight, PHP framework and allows you to cleanly structure your code
using MVC principles.
</p>

<p><a href="http://thedaylightstudio.com" target="_blank">Daylight Studio</a> developed FUEL CMS 
to be easily integrated into existing CodeIgniter projects allowing developers to select where 
and how they want to use the CMS. FUEL CMS is highly customizable and easily extensible.</p>

