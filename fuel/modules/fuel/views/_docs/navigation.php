<h1>Navigation</h1>
<p>The Navigation module creates a data structure that can be used with the <a href="<?=user_guide_url('libraries/menu')?>">Menu class</a> 
or <a href="<?=user_guide_url('helpers/fuel_helper')?>">fuel_nav</a> function.  It also provides you the ability to upload a file that 
contains a valid <a href="<?=user_guide_url('libraries/menu')?>">Menu Class</a> item array.
</p>
<p class="important">The Pages module provides an easy way to create navigation items when creating a page.</p>
